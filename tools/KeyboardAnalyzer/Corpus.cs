    public class Fre
    {
        public int Count;
        public decimal Freq;
    }


    public Dictionary<string, Fre> GetMonoGrams(string corpus)
    {
        Dictionary<string, Fre> grams = new Dictionary<string, Fre>();

        corpus = corpus
            .Replace("0", " ")
            .Replace("1", " ")
            .Replace("2", " ")
            .Replace("3", " ")
            .Replace("4", " ")
            .Replace("5", " ")
            .Replace("6", " ")
            .Replace("7", " ")
            .Replace("8", " ")
            .Replace("9", " ")
            .Replace("(", " ")
            .Replace(")", " ")
            .Replace("-", " ")
            .Replace(";", " ")
            .Replace(":", " ")
            .Replace("\"", " ")
            .Replace("\t", " ")
            .Replace("\r", "")
            .Replace("\n", " ")
            ;

        for (int i = 0; i < corpus.Length - 1; i++)
        {
            if (corpus[i] == ' ')
                continue;

            var word = $"{corpus[i]}";
            if (!grams.TryGetValue(word, out var val))
            {
                grams.Add(word, new Fre() { Count = 1 });
            }
            else
            {
                val.Count = val.Count + 1;
            }
        }

        var total = grams.Sum(x => x.Value.Count);
        foreach (var gra in grams)
        {
            gra.Value.Freq = gra.Value.Count * 100M / (decimal)total;
        }

        return grams;
    }

    public Dictionary<string, Fre> GetNgrams(string corpus, int n)
    {
        if (n < 1) throw new Exception("N less than 1");

        Dictionary<string, Fre> grams = new Dictionary<string, Fre>();

        corpus = corpus
            .Replace("0", " ")
            .Replace("1", " ")
            .Replace("2", " ")
            .Replace("3", " ")
            .Replace("4", " ")
            .Replace("5", " ")
            .Replace("6", " ")
            .Replace("7", " ")
            .Replace("8", " ")
            .Replace("9", " ")
            .Replace("(", " ")
            .Replace(")", " ")
            .Replace("-", " ")
            .Replace(";", " ")
            .Replace(":", " ")
            .Replace("\"", " ")
            .Replace("\t", " ")
            .Replace("\r", "")
            .Replace("\n", " ")
            ;

        for (int i = 0; i < corpus.Length - n; i++)
        {
            bool containsSpace = false;
            for (int j = 0; j < n;j++)
            {
                if (corpus[i+j]==' ')
                {
                    containsSpace = true;
                    break;
                }
            }
            if(containsSpace)
            {
                continue;
            }

            var word = corpus.Substring(i,n);
            if (!grams.TryGetValue(word, out var val))
            {
                grams.Add(word, new Fre() { Count = 1 });
            }
            else
            {
                val.Count = val.Count + 1;
            }
        }

        var total = grams.Sum(x => x.Value.Count);
        foreach (var gra in grams)
        {
            gra.Value.Freq = gra.Value.Count * 100M / (decimal)total;
        }

        return grams;
    }



